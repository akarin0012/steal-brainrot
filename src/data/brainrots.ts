function br(
  id: string, name: string, rarity: BrainrotDef['rarity'],
  income: number, cost: number, desc: string,
): BrainrotDef {
  return {
    id, name, rarity,
    baseIncomePerSec: income,
    cost,
    stealDifficulty: Math.ceil((RARITY_ORDER.indexOf(rarity) + 1) * 1.2),
    description: desc,
    color: RARITIES[rarity].color,
  };
}

